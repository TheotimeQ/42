int	add_op(int a, int b)
{
	return (a + b);
}

int	minus_op(int a, int b)
{
	return (a - b);
}

int	mult_op(int a, int b)
{
	return (a * b);
}

int	div_op(int a, int b)
{
	return (a / b);
}

int	mod_op(int a, int b)
{
	return (a % b);
}
